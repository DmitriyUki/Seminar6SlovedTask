int CountPositiveNumbers(int[] array) // метод по определению чисел больших чем 0
{
  int count = 0; // в данной строке скапливается колличество элементов
  for (int i = 0; i < array.Length; i++) // цикл с помомщью которого определяются элементы больше 0
  {
    if (array [i] > 0) // условие для определения больше число 0 или нет
    {
        count++; // в случае если условие выполнятеся, то счетчик увеличивается на 1
    }
  }
  return count; // возвращаем получившееся значение счетчика, после проведения исследования всех чисел цикла
}

