int[] GetRandomArray(int length) //метод по созданию массива чисел из тех чисел что ввел пользователь
{

    int[] array = new int[length]; // объявляем что создается новый массив и задам ему длину 

    for (int i = 0; i < array.Length; i++) // испольуем цикл, при помощи которого создаем массив
    {
        array[i] = ReadNumber($"Введите { i + 1 } - й элемент"); // создавая числа с идексами массив ипользуем числа введенные пользователем
    }
    return array; // выводи на экран получившийся массив
}
